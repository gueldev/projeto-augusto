#FUNÇÕES:

# procurar_carro(nome_carro, lista_de_carros): Essa função recebe o nome do carro desejado e a lista de carros disponíveis. Ela percorre a lista e verifica se o carro desejado está presente nela. Retorna "carro encontrado" se o carro estiver na lista, caso contrário, retorna "carro não encontrado".
# avaliacao_carro(preco_carro): Essa função recebe o preço do carro desejado e avalia sua condição com base nesse preço. Retorna uma string indicando a condição do carro: "Mal estado", "Conservado", "Seminovo" ou "Novo".
# main(): Essa função inicializa a lista de carros disponíveis e entra em um loop infinito onde solicita ao usuário o nome e o preço do carro desejado. Depois, imprime uma mensagem de confirmação com os dados fornecidos, e chama as funções procurar_carro() e avaliacao_carro() para verificar se o carro está disponível e avaliar sua condição, no fim oferece a opção de continuar ou encerrar o programa.

